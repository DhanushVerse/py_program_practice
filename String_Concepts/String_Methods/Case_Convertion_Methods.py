# 1.Upper
# The upper() method converts all characters in a string to uppercase.
msg = 'learning python is very easy'
result = msg.upper()
print(result)

# 2.Lower
# The lower() method converts all characters in a string to lowercase.
msg = 'LEARNING PYTHON IS VERY EASY'
result = msg.lower()
print(result)

# 3.title
# The title() method converts the first character of each word to upper case.
msg = 'learning python is very easy'
result = msg.title()
print(result)

# 4.Capitalize
# The capitalize() method converts the first character of a string to upper case and the rest to lower case.
msg = 'learning python is very easy'
result = msg.capitalize()
print(result)


